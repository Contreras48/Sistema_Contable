    private void cargarDatos(){
        //Inicializa la tabla
        modeloDiario = new DefaultTableModel(culumnas, 0);
        tblDiario.setModel(modeloDiario);
        
        int anchos[] = {400,20,20};
        for (int i = 0; i < tblDiario.getColumnCount() ;i++) {
            tblDiario.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            
        }
        
        //Genera el numero de partida
        diario = new LibroDiario();
        txtPartida.setText(String.valueOf(diario.obtenerPartida()));
        
        //Genera la fecha actual
        Date fecha = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        
        txtFecha.setText(formato.format(fecha));
    }
    
    private void limpiarFormulario(){
        txtCodigo.setText("");
        txtCuenta.setText("");
        txtCargo.setText("");
        txtAbono.setText("");
        txtDescripcion.setText("");
    }
    
    private boolean validarPartida(){
        boolean validar= false;
        if(!txtCodigo.getText().equals("") && (!txtCargo.getText().equals("") || !txtAbono.getText().equals(""))){
            validar = true;
        }
        return validar;
    }
    
    
    

