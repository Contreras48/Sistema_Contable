    private void btnEliminarMovimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMovimientoActionPerformed
        int fila;
        
        if (tblDiario.getSelectedRow() > -1) {
            fila = tblDiario.getSelectedRow();
            
            if(!tblDiario.getValueAt(fila, 1).equals("")){
                cargo = cargo - Double.parseDouble(String.valueOf(tblDiario.getValueAt(fila, 1)));
            }
            if(!tblDiario.getValueAt(fila, 2).equals("")){
                abono = abono - Double.parseDouble(String.valueOf(tblDiario.getValueAt(fila, 2)));
            }
 
            lblCargo.setText(" $"+cargo);
            lblAbono.setText(" $"+abono);
            
            modeloDiario.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado Ninguna cuenta");
        }
    }//GEN-LAST:event_btnEliminarMovimientoActionPerformed

    private void btnGuardarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPartidaActionPerformed
        int numRegistros = tblDiario.getRowCount();
        if(numRegistros >= 2){
            if(cargo == abono){
               int partida = Integer.parseInt(txtPartida.getText());
               String fecha = txtFecha.getText();
               String concepto = txtDescripcion.getText();
               int[] codigo = new int[numRegistros];
               double[] cargos = new double[numRegistros];
               double[] abonos = new double[numRegistros];
                for (int i = 0; i < numRegistros; i++) {
                    diario = new LibroDiario();

                    codigo[i] = Integer.parseInt(diario.buscar("codigo", String.valueOf(tblDiario.getValueAt(i, 0))));
                    if(!tblDiario.getValueAt(i, 1).equals("")){
                        cargos[i] = Double.parseDouble(String.valueOf(tblDiario.getValueAt(i, 1)));
                        abonos[i] = 0;
                    }else{
                        abonos[i] = Double.parseDouble(String.valueOf(tblDiario.getValueAt(i, 2)));
                        cargos[i] = 0;
                    }
                }
                 
                diario = new LibroDiario(partida, fecha, codigo, cargos, abonos, concepto);
                diario.registarPartida();
                
                cargarDatos();
                limpiarFormulario();
            }else{
                JOptionPane.showMessageDialog(null, "Los totales de cargo y abono no coinciden");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe de realizar almenos dos movimientos para guardar la partida");
        }
    }//GEN-LAST:event_btnGuardarPartidaActionPerformed

