    private void btnAgregarMovimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarMovimientoActionPerformed
        if(validarPartida()){
            if(!txtCargo.getText().equals("")){
                cargo = cargo + Double.parseDouble(txtCargo.getText());
            }
            if(!txtAbono.getText().equals("")){
                abono = abono + Double.parseDouble(txtAbono.getText());
            }        
            Object[] asientoDiario = {txtCuenta.getText(), txtCargo.getText(), txtAbono.getText()};

            modeloDiario.addRow(asientoDiario);
            lblCargo.setText(" $"+cargo);
            lblAbono.setText(" $"+abono);

            limpiarFormulario();
        }
    }//GEN-LAST:event_btnAgregarMovimientoActionPerformed

