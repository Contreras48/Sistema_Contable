    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila;
        
        if (tblCatalogo.getSelectedRow() > -1) {
            fila = tblCatalogo.getSelectedRow();
            
            int codigo = Integer.parseInt(String.valueOf(tblCatalogo.getValueAt(fila, 0)));
            
            catalogo = new CatalogoDeCuentas();
            catalogo.eliminarCuenta(codigo);
            
            //actualizamos el contenido de la tabla
            cuentas();
            
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado Ninguna cuenta");
        }
         
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(validar()) {
            int tipo = cmbTipo.getSelectedIndex();
            int codigo = Integer.parseInt(txtCodigo.getText());
            String nombre = txtNombre.getText();
            String descripcion = txtDescripcion.getText();
            int balance = cmbBalance.getSelectedIndex();

            if(accionGuardar==1){
                catalogo = new CatalogoDeCuentas(codigo, tipo, nombre, balance, descripcion);
                catalogo.agregarCuenta();

                JOptionPane.showMessageDialog(null, "La cuenta se ha registrado exitosamente", "Registro", 1);
            }else{
                catalogo = new CatalogoDeCuentas();
                catalogo.modificarCuenta(codigo, nombre, balance, descripcion);

                JOptionPane.showMessageDialog(null, "La cuenta se ha modificado exitosamente", "Modificación", 1);
            }
            
            limpiarFormulario();
            cuentas();
        }else{
            JOptionPane.showMessageDialog(null, "Por favor complete el formulario", "Error", 1);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

