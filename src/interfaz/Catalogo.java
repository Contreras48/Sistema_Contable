    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         int fila;
        
        if (tblCatalogo.getSelectedRow() > -1) {
            fila = tblCatalogo.getSelectedRow();
            
            //Preparamos el formulario
            
            //obtenemos el tipo de cuenta
            int tipoCuenta = 0;
            switch(tblCatalogo.getValueAt(fila, 1).toString()){
                case "Activo":
                    tipoCuenta = 1;
                    break;
                case "Pasivo":
                    tipoCuenta = 2;
                    break;
                case "Capital":
                    tipoCuenta = 3;
                    break;
            }
            
            //obtenemos el tipo de balance
            int tipoBalance = 0;
            switch(tblCatalogo.getValueAt(fila, 3).toString()){
                case "Normal":
                    tipoBalance = 1;
                    break;
                case "Ajuste":
                    tipoBalance = 2;
                    break;
            }
            
            cmbTipo.setSelectedIndex(tipoCuenta);
            txtCodigo.setText(tblCatalogo.getValueAt(fila, 0).toString());
            txtNombre.setText(tblCatalogo.getValueAt(fila, 2).toString());
            cmbBalance.setSelectedIndex(tipoBalance);
            txtDescripcion.setText(tblCatalogo.getValueAt(fila, 4).toString());
            
            //habilitamos los controles necesarios y establecemos la accion de modificar
            controles(1);
            cmbTipo.setEnabled(false);
            txtCodigo.setEnabled(false);
            accionGuardar = 2;
            
        }else{
            JOptionPane.showMessageDialog(null, "No se ha seleccionado Ninguna cuenta");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

