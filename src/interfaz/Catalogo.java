    public void limpiarFormulario(){
        //Limpiamos el formulario
        cmbTipo.setSelectedIndex(0);
        txtCodigo.setText("");
        txtNombre.setText("");
        cmbBalance.setSelectedIndex(0);
        txtDescripcion.setText("");
        
        //Bloquamos Algunos controles
        controles(0);
        
    }
    
    public void controles(int accion){
        switch(accion){
            //Si la accion es 0 bloquamos los controles
            case 0:
                btnAgregar.setEnabled(true);
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);
                btnGuardar.setEnabled(false);
                btnCancelar.setEnabled(false);
                
                cmbTipo.setEnabled(false);
                txtCodigo.setEnabled(false);
                txtNombre.setEnabled(false);
                cmbBalance.setEnabled(false);
                txtDescripcion.setEnabled(false);
                break;
            //Si la accion es 1 desbloquamos los controles
            case 1:
                btnAgregar.setEnabled(false);
                btnModificar.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnGuardar.setEnabled(true);
                btnCancelar.setEnabled(true);
                
                cmbTipo.setEnabled(true);
                txtCodigo.setEnabled(true);
                txtNombre.setEnabled(true);
                cmbBalance.setEnabled(true);
                txtDescripcion.setEnabled(true);
        }
    }
    
    public boolean validar(){
        int codTipo = cmbTipo.getSelectedIndex();
        int codBalance = cmbBalance.getSelectedIndex();
        
        return (codTipo != 0) & (codBalance != 0) & (!txtCodigo.getText().equals("")) & (!txtNombre.getText().equals("")) & (!txtDescripcion.getText().equals(""));
        
    }

